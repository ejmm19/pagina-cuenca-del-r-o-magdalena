							<div class="meta meta--full">
							</div>
							<p></p>
							
							<p>El clima predominante en la cuenca del Magdalena es el clima cálido, que abarca el 57% de toda la cuenca,
							 se caracteriza por presentar temperaturas entre los 28 °C y 32°C en promedio y es característico en la parte 
							 media y baja de la cuenca. Dependiendo si es cálido seco o cálido húmedo, habrá mayores o menores precipitaciones 
							 (CORMAGDALENA, 2007).</p>
							<p>Seguidamente el clima templado abarca el 22% de la cuenca, se presenta en la parte media y alta, sus temperaturas 
							medias oscilan entre 15°C y precipitaciones entre los 500 mm y 1000 mm anuales. Los demás tipos de climas: frío, muy 
							frío, extremadamente frío y nival, se localizan en las zonas más altas de las montañas que hacen parte de la cuenca, 
							sobre todo en las zonas del Macizo Colombiano y los Volcanes Nevados del Ruíz, Huila y algunas zonas de páramos.</p>
