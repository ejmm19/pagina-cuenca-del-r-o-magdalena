							 de 70.000 indígenas de las etnias Kogui, Arhuaco, Kankuamo y Wiwa (Arsarios). Es la montaña más alta del mundo a orillas 
							 del mar y tiene los dos picos más altos de Colombia, los picos Colón y Bolívar, con 5.775msnm . La Sierra fue declarada por 
							 la Unesco como Reserva de la Biosfera, del Hombre y de la Humanidad en el año de 1979 (Parques Nacionales Naturales de Colombia,
							  2015).</p>

