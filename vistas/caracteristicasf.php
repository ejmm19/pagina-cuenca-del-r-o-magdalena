							<h2 style="font-size:26px;" class="title title--full">Amenazas y Riesgos Naturales</h2>
							<div class="meta meta--full">	
							<br>
							<br>
							<br>
							<p></p>
							<p></p>
							<p>Las amenazas y riesgos naturales son condiciones que se presentan cuando la población habita ciertas zonas en donde
							 se producen fenómenos naturales que pueden causar daños en la infraestructura, pérdida de vidas humanas y daños al
							  medio ambiente. Tres conceptos son clave para entender las temáticas de riesgos naturales: amenaza, vulnerabilidad y 
							  riesgo.</p>			
								
							</div>
							<p></p>
							<p></p>
							
							    <div style="float:left;">
								<img src="../img/im4.2.7.gif" width="105%">
								</div>
								
							<p>Con base a la ley 1523 de 2012 se dan las siguientes definiciones: AMENAZA: Peligro latente de que un fenómeno natural
							 pueda causar pérdidas de vidas y daños a la infraestructura o al medio ambiente.
								VULNERABILIDAD: Susceptibilidad o fragilidad física, económica, social, ambiental o institucional que tiene 
								un elemento expuesto de ser afectado por una amenaza. RIESGO: Daños o pérdidas que pueden presentarse debido a los 
								eventos físicos peligrosos de origen natural, que son determinados por la vulnerabilidad de los elementos expuestos. 
								El riesgo puede ser alto, medio o bajo.</p>

							 <br><br><br>
							    <div style="float:left;">
								<img src="../img/americadecali.png" width="90%">
								</div>
								<div><h6><b>AMENAZA POR DESLIZAMIENTO </b></h6></div>
							<p>Los deslizamientos son movimientos pendiente abajo de material rocoso, depósitos no consolidados o suelo. Se dan en 
							áreas montañosas con pendientes entre los 5 a 30 grados. Son causados por eventos sísmicos, saturación del material por 
							altas precipitaciones y actividades antrópicas (Ministerio de Ambiente y Desarrollo Sostenible, 2014).</p>
							<br><br><br><br><br><br><br><br>
							    <div style="float:left;">
								<img src="../img/nlafont.jpg" width="90%">
								</div><br><br><br><br><br>
								
							<p>Analizando el mapa de amenaza de deslizamientos en la cuenca del río Magdalena, se observa que la amenaza alta
							 se da principalmente en el flanco occidental de la cordillera oriental, abarcando gran parte de los departamentos
							  de Boyacá, Cundinamarca y Santander. Las zonas con amenaza más baja corresponden a aquellas con un relieve plano, 
							  ubicadas principalmente en la parte baja de la cuenca en donde la llanura de inundación tiene la máxima amplitud.</p>
							<br><br><br><br><br><br><br><br><br><br><br><br><br><br>
							    <div style="float:left;">
								<img src="../img/mmm.jpg" width="90%">
								</div>
								<div><h6><b>AMENAZA POR INUNDACIÓN</b></h6></div>
							<p>Las inundaciones son un problema constante en el país en la época de lluvias, las zonas más susceptibles a inundarse 
							son las depresiones y partes más bajas de los valles fluviales. En la cuenca del Magdalena, la parte baja es la que 
							presenta mayores problemas de inundación, principalmente en la depresión Momposina y en las planicies de inundación 
							cercanas a la desembocadura del río.</p>
							
