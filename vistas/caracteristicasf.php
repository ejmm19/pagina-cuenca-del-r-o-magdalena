								</div>
								<div><h6><b>Conflictos de uso del suelo</b></h6></div>
							<p>Los conflictos de uso del suelo se presentan cuando el terreno es utilizado para una actividad que no le favorece, 
							por lo tanto se generan procesos degradativos que disminuyen la calidad de los ecosistemas (IGAC y CORPOICA, 2002). 
							Existen dos tipos de conflictos de uso del suelo: Conflicto por sobreutilización: se da cuando el suelo se utiliza más 
							allá de lo que puede soportar, es decir se hace un eso extra de lo que en realidad ofrece. Conflicto por subutilización: 
							se da cuando no se utiliza todo el potencial del suelo, sino que se ponen en práctica actividades que deterioran éste recurso. 
							Observando el mapa de conflictos de uso en la cuenca del magdalena, se nota que casi toda la cuenca presenta conflictos de 
							uso del suelo, asociados principalmente a la actividad ganadera en suelos muy fértiles (subutilización), y actividades
							 agrícolas en zona de alta pendiente (sobreutilización).</p>

							 <br><br><br><br><br><br><br><br>
							    <div style="float:left;">
								<img src="../img/im4.2.5.jpg" width="90%">
								</div>
								<div><h6><b>Contaminación hídrica</b></h6></div>
							<p>Las ciudades que aportan un mayor número de contaminantes al río Magdalena y sus afluentes son: Bogotá, Barranquilla, 
							Pereira, Bucaramanga, Cartagena, Santa Marta y Manizales (CORMAGDALENA, 2007). El caso más crítico por contaminación hídrica 
							en la cuenca del Magdalena, se da en río Bogotá, el río más contaminado al país, el cual diariamente vierte sus aguas
							 contaminadas al río Magdalena. Las principales causas de contaminación del río Bogotá son la industria de curtiembres
							  en el municipio de Villapinzón, Cundinamarca, y la contaminación generada por el vertimiento de desechos y aguas residuales
							   a su paso por Bogotá y otros municipios (CAR, 2006). En el año 2014, el Consejo de Estado ordenó a 72 entidades estatales 
							   la descontaminación del río Bogotá, para ello dichas entidades deberán invertir cerca de 6 billones de pesos
							    en este proyecto.</p>
							
							
							

