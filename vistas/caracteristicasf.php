							<h1 style="font-size:26px;" class="title title--full">Ecosistema</h1>
							
							
							<p>Los principales ecosistemas en la cuenca del Magdalena corresponden a: bosques, páramos, xerófitas y 
							humedales. Los bosques cubren 7.195.867 hectáreas, lo que constituye el 26,3% del área de la cuenca (CORMAGDALENA, 2007).</p>
							<br><br>
							
							<div style="float:left;">
								<img src="../img/im2.4.1.jpg" width="70%">
							</div><h6><b>Bosque seco tropical</b></h6>
							<p>El bosque seco tropical es propio en tierras bajas y se caracteriza por presentar una fuerte estacionalidad de lluvias.
							 El BST tiene una biodiversidad única de plantas y animales que se han adaptado a condiciones de estrés hídrico, por lo cual
							  presenta altos niveles de endemismo, es decir que contiene especies que no se dan en ningún otro tipo de ecosistema
							   (Pizano y García, 2014).</p>
								
								<br><br><br><br><br><br>
								<div>
									
								<div style="float:left;">
								<img src="../img/im2.4.2.jpg" width="70%">
								</div>
							
								</div><h6><b>Bosque seco tropical</b></h6>
								<p>Es el bioma más complejo de la Tierra en términos de su estructura y diversidad de especies. Ocurre bajo condiciones 
							ambientales óptimas para la vida: disponibilidad de calor durante todo el año y abundante precipitación (Universidad Nacional
							 de Colombia, s.f.) Las precipitaciones en este ecosistema alcanzan hasta los 8.000 mm/año. Sin embargo, este ambiente tan
							  exuberante y diverso es frágil, los suelos que hacen parte de estos ecosistemas son ácidos y poco fértiles.</p>

							  <br><br><br><br>
								<div>
									
								<div style="float:left;">
								<img src="../img/depre.jpg" width="70%">
								</div>
							
								</div><h6><b>Humedales en la depresión Momposina</b></h6>
								<p>La depresión Momposina está conformada por 11 municipios en 4 departamentos: Córdoba, Sucre, Bolívar 
								y Antioquía. Los humedales abarcan el delta interior conformado por la confluencia de los ríos Cauca, San Jorge y 
								Magdalena y su complejo de ciénagas (Ministerio de Ambiente y Desarrollo Sostenible, 2013).</p>

