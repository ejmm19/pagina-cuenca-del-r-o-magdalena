							<h2 style="font-size:26px;" class="title title--full">Relieve y suelos de la cuenca del Magdalena</h2>
							<div class="meta meta--full">				
								
							</div>
							<p></p>
							<p>En la parte alta de la cuenca se presenta un relieve montañoso y escarpado. Los suelos en esta
							 zona son en su mayoría de origen volcánico, negros, con buen contenido de materia orgánica. Éstos 
							 suelos presentan un pH bajo y son ricos en nutrientes, por lo tanto son fértiles, estos suelos hacen 
							 parte del orden de los Andisoles (Moreno et al, 2011).</p>
							<p></p>
							<p>La parte media de la cuenca presenta un relieve un poco menos abrupto, el valle del Magdalena presenta 
							su mayor amplitud. Las zonas planas del valle interandino, se encuentran bordeadas por las cordilleras central
							 y oriental. Debido a la presencia de rocas ricas en cuarzo en esta zona, los suelos son ácidos y con poca
							  fertilidad pero, a diferencia de la parte alta, la pluviosidad es mayor, y existen algunas zonas que disponen 
							  de una buena cantidad de nutrientes como en la región de Puerto Boyacá (León, s.f.).</p>
							 <p>En la parte baja de la cuenca, el paisaje que predomina es la llanura aluvial, también se encuentran ciénagas,
							  pantanos y colinas al borde del cauce, enriqueciendo el paisaje natural de la zona. El paisaje de planicie es la
							   unidad con mayor representación en esta zona del río, en esta zona se encuentran depresiones como La Mojana, 
							   una zona deprimida en donde se han formado complejos sistemas de humedales (IDEAM, 2010). Los suelos de las 
							   planicies aluviales y zonas de origen lacustre, son profundos y se alimentan constantemente de los sedimentos 
							   transportados por los grandes ríos que cruzan la región, los cuales se componen en general de mezclas de materiales 
							   muy recientes.</p>
